#Show the type of variables on the screen using type().
# Numbers
a = 10           # int
b = 3.14         # float
c = -5           # negative int 
d = 2 + 3j       # complex (complex number)

# Text (strings)
e = "Hello, world" # str
f = ''           # empty string 

# Booleans
g = True         # bool
h = False        # bool

# Colections
i = [1, 2, 3]               # list
j = (4, 5, 6)               # tuple
k = {7, 8, 9}               # set
l = {'nome': 'Ana'}        # dict
m = range(5)               # range

# Special Values
n = None        # NoneType

# Binary e bytes
o = b'abc'      # bytes
p = bytearray(5) # bytearray

# Others
q = frozenset([1, 2, 3]) # frozenset
